#!/bin/bash

mv $1 $1.tmp
wc -l $1.tmp|awk '{print $1}' > $1
echo -e "" >> $1
gawk '$2 == "1" {print "H",$4,$5,$6};
      $2 == "5" {print "B",$4,$5,$6};
      $2 == "6" {print "C",$4,$5,$6};
      $2 == "7" {print "N",$4,$5,$6};
      $2 == "8" {print "O",$4,$5,$6};
      $2 == "16" {print "S",$4,$5,$6};
      $2 == "29" {print "Cu",$4,$5,$6};
      $2 == "35" {print "Br",$4,$5,$6};
      $2 == "80" {print "Hg",$4,$5,$6};
      $2 == "17" {print "Cl",$4,$5,$6}' $1.tmp >> $1

